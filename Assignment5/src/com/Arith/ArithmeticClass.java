package com.Arith;

public class ArithmeticClass {
	public int add(int a,int b) {
		return a+b;
		
	}
	public int sub(int a,int b) {
		return a-b;
		
	}
	public int mul(int a,int b) {
		return a*b;
		
	}
	public double div(int a,int b) {
		return a/b;
		
	}
	public double modular(int a,int b) {
		return a%b;
		
	}
	
}

